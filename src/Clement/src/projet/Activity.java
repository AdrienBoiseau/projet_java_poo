package projet;

public class Activity {
    public String name;
    public int duration;

    public Activity(String name, int duration) {
        /**
         * A simple class represent an activity with a duration.
         *
         * @name : a string which represent the name of the activity.
         * @duration : an integer which represent the duration of the activity.
         */
        this.name = name;
        this.duration = duration;
    }
}
