package projet;

public class Activity {
    String namming;
    int duration;

    public Activity(String namming, int duration) {
        /**
         * A simple class represent an activity with a duration.
         *
         * @namming : a string which represent the name of the activity.
         * @duration : an integer which represent the duration of the activity.
         */
        this.namming = namming;
        this.duration = duration;
    }
}
